# Strings Slicing and Operations on String

fruit = "Mango"
mangoLen = len(fruit)
print(mangoLen)    # Length of a string
print(fruit[0:4])
print(fruit[:4])   # String as an Array
print(fruit[:])
print(fruit[:-3])  #print(fruit[:len(fruit)-3])
print(fruit[-1:-3]) #print(fruit[-1:len(fruit)-3])
print(fruit[-3:-1]) 

nm = "Harry"
print(nm[-4:-2])